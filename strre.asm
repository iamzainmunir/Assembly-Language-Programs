.MODEL SMALL
.STACK 100h
.DATA
STR1 DB 20 DUP(?),"$"
.CODE

MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV ES,AX
MOV DI,OFFSET STR1
MOV CX,LENGTHOF STR1
DEC CX


INPUT:
MOV AH,1
INT 21H
CMP AL,13
JE MEND
STOSB


LOOP INPUT


MEND:

MOV CX,LENGTHOF STR1
DEC CX
MOV SI,0

ENCRYPT:
CMP STR1[SI],'e'
JE CODE1

CMP STR1[SI],'a'
JE CODE2

JMP AGEYBARO

CODE1:
MOV STR1[SI],'3'
JMP AGEYBARO
CODE2:
MOV STR1[SI],'@'

AGEYBARO:
INC SI

LOOP ENCRYPT









MOV AH,9
MOV DX,OFFSET STR1
INT 21H










MOV AH,4CH
INT 21H



MAIN ENDP
END MAIN
